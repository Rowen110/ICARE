package com.icare.common.masterdata;

public class MajorDiagnosticCategory {
	private String mdcNumber;
	private String mdcDesc;
	private String mdcSubGroupDesc;
	private String mdcSubGroupStart;
	private String mdcSubGroupEnd;
	public String getMdcNumber() {
		return mdcNumber;
	}
	public void setMdcNumber(String mdcNumber) {
		this.mdcNumber = mdcNumber;
	}
	public String getMdcDesc() {
		return mdcDesc;
	}
	public void setMdcDesc(String mdcDesc) {
		this.mdcDesc = mdcDesc;
	}
	public String getMdcSubGroupDesc() {
		return mdcSubGroupDesc;
	}
	public void setMdcSubGroupDesc(String mdcSubGroupDesc) {
		this.mdcSubGroupDesc = mdcSubGroupDesc;
	}
	public String getMdcSubGroupStart() {
		return mdcSubGroupStart;
	}
	public void setMdcSubGroupStart(String mdcSubGroupStart) {
		this.mdcSubGroupStart = mdcSubGroupStart;
	}
	public String getMdcSubGroupEnd() {
		return mdcSubGroupEnd;
	}
	public void setMdcSubGroupEnd(String mdcSubGroupEnd) {
		this.mdcSubGroupEnd = mdcSubGroupEnd;
	}
	
}
